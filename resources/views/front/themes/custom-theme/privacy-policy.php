			<h3>What is a privacy policy?</h3>
            <p>A privacy policy is a document that states what personal data you collect from your users, why, and how you keep it private.</p>
            <p>The purpose of the privacy policy is to inform your users about how their data is being handled.</p>
            <p>Hence, the privacy policy should be accessible for your users and kept in a plain and readable language.</p> 
            <p><strong>Most countries</strong> have privacy laws requiring that websites collecting personal data have a proper privacy policy in place.</p>
            <p>Failure to comply can result in heavy fines and even prosecution. Are you based in the EU or providing services to EU citizens, you must have a GDPR-compliant privacy policy on your domain.</p>
            <p>We will get into this in more detail below.</p>

            <h3>What is personal data?</h3>
            <p><strong>Personal data</strong> is information that can identify an individual, either directly or when combined with other data.</p>
            <p>Names, e-mails, addresses, localization, IP-addresses, photos, and account information all are directly identifying data.</p>
            <p>Health information, income, religion and cultural profiles and the like is also personal data.</p>
            <p>Furthermore, and crucial in the present context, data on user behavior is also personal. Cookies can track and register individual usersâ€™ browsing activities, like what articles they scroll past and which ones they choose to click on.</p>

            <h3>Do I need a privacy policy for my website?</h3>
            <p>You probably do. If your website collects personal data, you need a privacy policy.</p>
            <p>Most websites collect user data. Often, it happens without the website owner even being aware of it, by means of <strong>cookies</strong>.</p>
            <p>If your website is hosted, or if you use plugins, social media-buttons, analytics tools and the like on your website, then it does set cookies and collect user data.</p>
